// TIP: ES5 VAR : variables
// permite que cambien en cualquier momento
var nombre5 = 'Jorge';
var edad5 = 333;
console.log(`nombre5`, nombre5, edad5);
nombre5 = 'carlos';
edad5 = 334;
console.log(`nombre5`, nombre5, edad5);

// TIP: ES6 LET, CONST : Variables
// const permite definir constantes
// let permite definir variable cambiante
const nombre6 = 'Ana';
let edad6 = 444;
console.log(`nombre6`, nombre6, edad6);
nombre6 = 'Maria';
edad6 = 445;
console.log(`nombre6`, nombre6, edad6);
