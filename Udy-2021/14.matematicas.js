/***
 * Funciones matematicas
 */

// TIP: math.abs : valor absoluto
console.log(` Math.abs(-45) : `, Math.abs(-45));

// TIP: math.pow : potencias
console.log(` Math.pow(2,3) : `, Math.pow(2, 3));

// TIP: math.sqrt : Raiz cuadrada
console.log(` Math.sqrt(9) : `, Math.sqrt(9));

// TIP: math.round :redondear
console.log(` Math.round(12.1234) : `, Math.round(12.1234));

// TIP: math.random : aleatorio
console.log(` Math.random() : `, Math.random());

// TIP: math.sign : indica si es positivo (1) o negativo (0)
console.log(` Math.sign(-8) : `, Math.sign(-8));

// TIP: math.ceil : redondea al superior
console.log(` Math.ceil(2.149) : `, Math.ceil(2.149));

// TIP: math.floor : redondea al inferior
console.log(` Math.floor(2.149) : `, Math.floor(2.149));
